	<h2 class="md:text-left text-center">History of Odd Fellowship</h2>
	<section>
		<h3>Origins and Nature of the Institution</h3>

		<p>
			The Order of Odd Fellows is a benevolent and social society, sometimes classified as a
			friendly benefit society having initiatory rites and ceremonies, gradation or degrees in
			membership, and mystic signs of recognition and communication.
		</p>

		<p>
			While Odd Fellowship is not a religious institution, many of its principles, tenets,
			practices, and objectives are based upon the teachings of the Holy Bible. Many of the rites
			and ceremonies, of ritual and lectures, the secret passwords, signs, and counter-signs, have a
			Biblical origin or significance.
		</p>

		<p>
			Any friendly and benevolent society is a mutual association of individuals which has as its
			chief purpose the welfare of its members. One of its primary aims is to provide its members
			with aid when suffering for the needs of life because of illness, unemployment, or other
			misfortunes. The relief or sustenance of members, of their families and close relatives, of
			their widows and orphans in case of death, appears to have been the chief purpose of the
			organization of Odd Fellowship in its beginning. These aims and purposes have been
			consistently and faithfully maintained throughout the history of the Order.
		</p>

		<p>
			By 1796 Odd Fellow organizations were numerous in England, and each was independent from the
			others. Fraternal groups such as the Odd Fellows were suppressed in England for a time, but by
			1803 the Odd Fellows were revived by an organization called "London Union Odd Fellows," which
			later became known as the "Grand Lodge of England" and assumed authority over all Odd Fellow
			lodges in that country.
		</p>

		<p>
			Victory Lodge in Manchester declared itself independent of the Grand Lodge of England in 1809.
			In 1814, the six Odd Fellows lodges in the Manchester area met and formed The Manchester Unity
			of the Independent Order of Odd Fellows, which elected officers and proceeded to standardize
			degree work of the lodges.
		</p>
	</section>
	<section>
		<h3>Why the Name Odd Fellows?</h3>

		<p>
			There are several different reasons given for our strange name. One old and apparently
			authoritative history of Odd Fellowship gives the explanation, "That common laboring men
			should associate themselves together and form a fraternity for social unity and fellowship and
			for mutual help was such a marked violation of the trends of the times (England in the 1700's)
			that they became known as 'peculiar' or 'odd,' and hence they were derided as 'Odd Fellows.'
			Because of the appropriateness of the name, those engaged in forming these unions accepted it.
			When legally incorporated the title 'Odd Fellows' was adopted."
		</p>

		<p>
			Another, similar explanation is that the original Odd Fellows were men who were engaged in
			various or odd trades, as there were organizations for some of the larger trades.
		</p>

		<p>
			Modern references state that the true reason for the name Odd Fellows isn't known or
			documented. Whatever the reason may have been, the unusual name has been the object of public
			curiosity (and on occasion derision or mirth) for well over 200 years.
		</p>
	</section>
	<section>
		<h3>Odd Fellowship In North America</h3>

		<p>
			Among the first records of the Order in America is that of five Brothers of the English Order
			who met in New York City in 1806 and formed Shakespeare Lodge No. 1.
		</p>

		<p>
			The founders were three boat builders, a comedian, and a vocalist - a group befitting the name
			"Odd Fellows," indeed. The lodge was self-instituted, a common practice in those times. Their
			first candidate was a retired actor who was the keeper of the tavern where they met. Accounts
			state that lodge meetings were accompanied by merry making and mirth and that the wares of the
			tavern were freely indulged in. This lodge was dissolved in 1813 due to poor attendance
			brought on by controversy over the War of 1812.
		</p>

		<p>
			Another lodge of which little is known existed briefly in New York in 1816. In 1818,
			Shakespeare Lodge in New York was re-instituted in the Red Cow tavern, operated by a former
			member who had in his keeping the books and papers of the former lodge. They claimed to have
			received a charter from the Manchester Unity which gave them authority over all other Odd
			Fellows Lodges in the United States, but this authority was not accepted by other lodges.
			Several more lodges were founded in the New York City area and one in Philadelphia, due to the
			efforts of the Brothers of Shakespeare Lodge.
		</p>

		<p>
			The Independent Order of Odd Fellows as we know it today began in Baltimore, Maryland, where
			five members of the Order from England founded Washington Lodge No. 1 on April 26,1819, by
			self-institution. One of these Brothers was Thomas Wildey, the first Noble Grand and the man
			revered as the founder of Odd Fellowship in North America. A charter was received from Duke of
			York Lodge in Preston, England, in 1820, a year and a half after its self-institution.
		</p>

		<p>
			A second lodge was formed in Baltimore in 1819, but these two lodges and those in New York
			were unaware of each others' existence for some time, communications being slow in those days,
			and there being no reason such information would travel from one city to another except by
			pure chance.
		</p>

		<p>
			In 1821, the "Grand Lodge of Maryland and of the United States of America, of the Independent
			Order of Odd Fellows," was founded. Brother Wildey also served as the first Grand Master/Grand
			Sire of the first Grand Lodge, for a period of 12 years. Several more lodges were established,
			and in 1824, the "Grand Lodge of the United States" now termed "The Sovereign Grand Lodge,"
			was separated from the Grand Lodge of Maryland. The Independent Order of Odd Fellows in North
			America (United States and Canada) became independent from the Order in England in 1834.
		</p>
	</section>
